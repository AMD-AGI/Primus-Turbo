"""SCORING HARNESS (immutable ruler) — FlyDSL attention for GPT-OSS, fwd+bwd.

This is the campaign's benchmark/verify contract. DO NOT EDIT THIS FILE as part of
any optimization round — it defines the score and the correctness gate; editing it
to inflate the number is forbidden and will be reverted.

What it measures
----------------
GPT-OSS-20b/120b attention shape: GQA num_heads=64 / num_kv_heads=8, head_dim=64,
bf16, causal. Reports a single combined fwd+bwd TFLOPS ("tflops") at batch=1,
seq=8192 (the scored number), plus fwd/bwd TFLOPS separately (informational).

The kernel-under-test is the FlyDSL attention backend the campaign builds:

    primus_turbo/pytorch/kernels/attention/attention_flydsl_impl.py
        attention_flydsl_forward_impl(q, k, v, softmax_scale, causal) -> (out, lse)
            q:[B,S,Hq,D]  k,v:[B,S,Hkv,D]  bf16, bshd, contiguous
            out:[B,S,Hq,D] bf16 ; lse:[B,Hq,S] float32
        attention_flydsl_backward_impl(dout, q, k, v, out, lse, softmax_scale, causal)
            -> (dq, dk, dv)   same shapes/dtype as q,k,v

BOOTSTRAP: whichever direction is not yet implemented (module/function missing, or
raises) falls back to a torch reference for THAT direction, so the harness always
produces a valid baseline number. As the campaign lands the FlyDSL fwd, then bwd,
the combined TFLOPS rises and each piece is kept. A FlyDSL piece that IS present
but fails the correctness gate makes the whole run ok=false (a wrong kernel is
never a valid win). "fwd_from"/"bwd_from" in the JSON say which path was measured;
a run where both are "flydsl" and ok=true is a fully-native FlyDSL result.

Correctness: checked at small shapes (exact fp32 reference) so the fp32 scores fit
in memory; timing is done at the large scored shape.

Determinism (race) gate: for the FlyDSL path only, the same fixed input is run
DET_RUNS_FWD/DET_RUNS_BWD times and every run must be bit-identical (rtol=atol=0)
to run#0 (myskill: det=0 over >=500 runs proves no race). A FlyDSL kernel that is
non-deterministic fails the whole run (ok=false). The torch fallback is NOT
det-gated (torch SDPA bwd uses atomics and isn't bit-reproducible). Tune run
counts via env DET_RUNS_FWD / DET_RUNS_BWD (0 disables that direction's check).
"""
import json
import math
import os
import sys
import traceback

import torch
import torch.nn.functional as F

BF16 = torch.bfloat16
DEV = "cuda"

# GPT-OSS attention: GQA 64/8, head_dim 64. Standard causal (full-attn layer;
# sliding-window / attention-sink are out of scope for this perf kernel).
H_Q, H_KV, D = 64, 8, 64
SCORED_B, SCORED_S = 1, 8192            # the scored (timed) point
FWD_TARGET_TFLOPS, BWD_TARGET_TFLOPS = 1150.0, 750.0   # separate fwd/bwd goals
# Separate fwd/bwd targets: the scored "score" normalizes each direction by its
# own target so fwd is not drowned out by the time-weighted combined tflops.
FWD_TARGET_TFLOPS, BWD_TARGET_TFLOPS = 1150.0, 750.0
CORR_CASES = [(2, 512), (2, 1024)]      # (b, s) exact-fp32 correctness points

# determinism (race) gate — myskill: det=0 bit-exact (rtol=atol=0) over >=500 runs
# on a FIXED input proves no race. Only enforced for the FlyDSL path (a racy kernel
# is never a valid win); the torch fallback is not det-gated (torch SDPA bwd uses
# atomics and is not bit-reproducible, which must not fail the bootstrap baseline).
DET_RUNS_FWD = int(os.environ.get("DET_RUNS_FWD", "500"))
DET_RUNS_BWD = int(os.environ.get("DET_RUNS_BWD", "200"))
DET_B, DET_S = 1, 8192                   # det check at the real (scored) workload shape


# --------------------------------------------------------------------------- #
# FlyDSL backend under test (lazy; falls back to torch per-direction if absent) #
# --------------------------------------------------------------------------- #
def _load_flydsl():
    fwd = bwd = None
    try:
        from primus_turbo.pytorch.kernels.attention.attention_flydsl_impl import (
            attention_flydsl_forward_impl as fwd,
        )
    except Exception:
        fwd = None
    try:
        from primus_turbo.pytorch.kernels.attention.attention_flydsl_impl import (
            attention_flydsl_backward_impl as bwd,
        )
    except Exception:
        bwd = None
    return fwd, bwd


# --------------------------------------------------------------------------- #
# torch reference (exact fp32 attention; autograd gives exact ref grads)       #
# --------------------------------------------------------------------------- #
def _ref_fwd_fp32(q, k, v, scale, causal):
    b, s, hq, d = q.shape
    hkv = k.shape[2]
    qf = q.float().transpose(1, 2)                                   # b,hq,s,d
    kf = k.float().transpose(1, 2).repeat_interleave(hq // hkv, 1)   # b,hq,s,d
    vf = v.float().transpose(1, 2).repeat_interleave(hq // hkv, 1)
    scores = torch.matmul(qf, kf.transpose(-1, -2)) * scale         # b,hq,s,s
    if causal:
        idx = torch.arange(s, device=q.device)
        scores = scores.masked_fill(idx[None, None, :, None] < idx[None, None, None, :], float("-inf"))
    p = scores.softmax(-1)
    o = torch.matmul(p, vf)                                          # b,hq,s,d
    return o.transpose(1, 2)                                         # b,s,hq,d


def _cos_l2(x, ref):
    """min per-row (over D) cosine similarity, and relative L2 error, in fp32.

    Rows whose REFERENCE is (near-)zero are excluded from the min-cosine: they are
    legitimately zero (e.g. causal dq at the first query position — a single-element
    softmax has zero gradient), and cosine(0,0) is undefined (torch returns 0), which
    would wrongly fail a mathematically-exact gradient. A candidate that is NOT ~zero
    where the reference is ~zero is still caught by the relative-L2 term (which uses
    the full tensors), so this only removes the false failure, not real errors.
    """
    x = x.float().reshape(-1, x.shape[-1])
    ref = ref.float().reshape(-1, ref.shape[-1])
    l2 = (x - ref).norm().item() / (ref.norm().item() + 1e-12)
    ref_norm = ref.norm(dim=1)
    mask = ref_norm > 1e-6 * (ref_norm.max() + 1e-12)
    if mask.any():
        cos = F.cosine_similarity(x[mask], ref[mask], dim=1).min().item()
    else:
        cos = 1.0
    return cos, l2


# --------------------------------------------------------------------------- #
# timing                                                                       #
# --------------------------------------------------------------------------- #
def _time(fn, warmup=10, iters=30):
    for _ in range(warmup):
        fn()
    torch.cuda.synchronize()
    e0 = [torch.cuda.Event(enable_timing=True) for _ in range(iters)]
    e1 = [torch.cuda.Event(enable_timing=True) for _ in range(iters)]
    for i in range(iters):
        e0[i].record()
        fn()
        e1[i].record()
    torch.cuda.synchronize()
    ts = sorted(e0[i].elapsed_time(e1[i]) for i in range(iters))
    return ts[len(ts) // 2] * 1e-3  # seconds


# --------------------------------------------------------------------------- #
# correctness gate                                                             #
# --------------------------------------------------------------------------- #
def _check_correctness(fwd, bwd, scale):
    """Return (ok, notes). Only checks directions that use the FlyDSL path."""
    notes = {}
    for b, s in CORR_CASES:
        torch.manual_seed(0)
        q = torch.randn(b, s, H_Q, D, device=DEV, dtype=BF16)
        k = torch.randn(b, s, H_KV, D, device=DEV, dtype=BF16)
        v = torch.randn(b, s, H_KV, D, device=DEV, dtype=BF16)
        dout = torch.randn(b, s, H_Q, D, device=DEV, dtype=BF16)

        # fp32 reference (exact) + ref grads via autograd
        qg = q.float().clone().requires_grad_(True)
        kg = k.float().clone().requires_grad_(True)
        vg = v.float().clone().requires_grad_(True)
        ref_out = _ref_fwd_fp32(qg, kg, vg, scale, True)
        ref_out.backward(dout.float())
        ref_dq, ref_dk, ref_dv = qg.grad, kg.grad, vg.grad

        if fwd is not None:
            out, lse = fwd(q, k, v, scale, True)
            cos, l2 = _cos_l2(out, ref_out)
            notes[f"fwd_b{b}s{s}"] = {"cos": round(cos, 5), "l2": round(l2, 5)}
            if not (cos > 0.99 and l2 < 0.03):
                return False, {"fail": f"fwd cos={cos:.4f} l2={l2:.4f} @b{b}s{s}", **notes}
        if bwd is not None and fwd is not None:
            out, lse = fwd(q, k, v, scale, True)
            dq, dk, dv = bwd(dout, q, k, v, out, lse, scale, True)
            for nm, x, r in (("dq", dq, ref_dq), ("dk", dk, ref_dk), ("dv", dv, ref_dv)):
                cos, l2 = _cos_l2(x, r)
                notes[f"{nm}_b{b}s{s}"] = {"cos": round(cos, 5), "l2": round(l2, 5)}
                if not (cos > 0.98 and l2 < 0.05):
                    return False, {"fail": f"{nm} cos={cos:.4f} l2={l2:.4f} @b{b}s{s}", **notes}
    return True, notes


# --------------------------------------------------------------------------- #
# determinism / race gate (FlyDSL path only): fixed input, N runs, bit-exact   #
# --------------------------------------------------------------------------- #
def _check_determinism(fwd, bwd, scale):
    """Return (ok, notes). det=0 means every run is bit-identical to run#0."""
    notes = {}
    if fwd is None and bwd is None:
        return True, {"skipped": "no flydsl path"}
    b, s = DET_B, DET_S
    torch.manual_seed(1234)
    q = torch.randn(b, s, H_Q, D, device=DEV, dtype=BF16)
    k = torch.randn(b, s, H_KV, D, device=DEV, dtype=BF16)
    v = torch.randn(b, s, H_KV, D, device=DEV, dtype=BF16)
    dout = torch.randn(b, s, H_Q, D, device=DEV, dtype=BF16)

    if fwd is not None:
        out0, lse0 = fwd(q, k, v, scale, True)
        out0, lse0 = out0.clone(), lse0.clone()
        bad = 0
        for _ in range(DET_RUNS_FWD):
            o, l = fwd(q, k, v, scale, True)
            if not (torch.equal(o, out0) and torch.equal(l, lse0)):
                bad += 1
        notes["fwd"] = {"runs": DET_RUNS_FWD, "nondet": bad}
        if bad:
            return False, {"fail": f"fwd nondeterministic: {bad}/{DET_RUNS_FWD} runs differ", **notes}

    if bwd is not None and fwd is not None:
        out, lse = fwd(q, k, v, scale, True)
        dq0, dk0, dv0 = bwd(dout, q, k, v, out, lse, scale, True)
        dq0, dk0, dv0 = dq0.clone(), dk0.clone(), dv0.clone()
        bad = 0
        for _ in range(DET_RUNS_BWD):
            dq, dk, dv = bwd(dout, q, k, v, out, lse, scale, True)
            if not (torch.equal(dq, dq0) and torch.equal(dk, dk0) and torch.equal(dv, dv0)):
                bad += 1
        notes["bwd"] = {"runs": DET_RUNS_BWD, "nondet": bad}
        if bad:
            return False, {"fail": f"bwd nondeterministic: {bad}/{DET_RUNS_BWD} runs differ", **notes}
    return True, notes


# --------------------------------------------------------------------------- #
# scored timing (large shape); per-direction FlyDSL-or-torch                   #
# --------------------------------------------------------------------------- #
def _measure(fwd, bwd, scale):
    b, s = SCORED_B, SCORED_S
    torch.manual_seed(0)
    q = torch.randn(b, s, H_Q, D, device=DEV, dtype=BF16)
    k = torch.randn(b, s, H_KV, D, device=DEV, dtype=BF16)
    v = torch.randn(b, s, H_KV, D, device=DEV, dtype=BF16)
    dout = torch.randn(b, s, H_Q, D, device=DEV, dtype=BF16)

    fwd_flops = 2.0 * b * H_Q * s * s * D          # causal: 2*b*hq*s^2*(d+d)*0.5
    bwd_flops = 2.5 * fwd_flops

    # ---- forward ----
    if fwd is not None:
        fwd_from = "flydsl"
        out, lse = fwd(q, k, v, scale, True)
        fwd_t = _time(lambda: fwd(q, k, v, scale, True))
    else:
        fwd_from = "torch"
        qg = q.clone().requires_grad_(True)
        kg = k.clone().requires_grad_(True)
        vg = v.clone().requires_grad_(True)

        def torch_fwd():
            return F.scaled_dot_product_attention(
                qg.transpose(1, 2), kg.transpose(1, 2), vg.transpose(1, 2),
                is_causal=True, scale=scale, enable_gqa=True).transpose(1, 2)

        out = torch_fwd()
        lse = None
        fwd_t = _time(lambda: F.scaled_dot_product_attention(
            q.transpose(1, 2), k.transpose(1, 2), v.transpose(1, 2),
            is_causal=True, scale=scale, enable_gqa=True))

    # ---- backward ----
    if bwd is not None and fwd is not None:
        bwd_from = "flydsl"
        bwd_t = _time(lambda: bwd(dout, q, k, v, out, lse, scale, True))
    else:
        bwd_from = "torch"
        qg = q.clone().requires_grad_(True)
        kg = k.clone().requires_grad_(True)
        vg = v.clone().requires_grad_(True)
        o = F.scaled_dot_product_attention(
            qg.transpose(1, 2), kg.transpose(1, 2), vg.transpose(1, 2),
            is_causal=True, scale=scale, enable_gqa=True).transpose(1, 2)

        def torch_bwd():
            qg.grad = kg.grad = vg.grad = None
            o.backward(dout, retain_graph=True)

        bwd_t = _time(torch_bwd)

    fwd_tf = fwd_flops / fwd_t / 1e12
    bwd_tf = bwd_flops / bwd_t / 1e12
    combined = (fwd_flops + bwd_flops) / (fwd_t + bwd_t) / 1e12
    # Scored objective: sum of per-direction attainment vs separate targets
    # (x100 for readable logs). fwd and bwd count toward their OWN target, so
    # forward is no longer drowned out by the time-weighted combined metric.
    score = 100.0 * (fwd_tf / FWD_TARGET_TFLOPS + bwd_tf / BWD_TARGET_TFLOPS)
    return {
        "score": round(score, 3),
        "tflops": round(combined, 2),
        "fwd_tflops": round(fwd_tf, 2),
        "bwd_tflops": round(bwd_tf, 2),
        "fwd_from": fwd_from,
        "bwd_from": bwd_from,
        "b": b, "s": s,
    }


def main():
    scale = 1.0 / math.sqrt(D)
    result = {"ok": False, "score": 0.0, "tflops": 0.0}
    try:
        fwd, bwd = _load_flydsl()
        ok, notes = _check_correctness(fwd, bwd, scale)
        if not ok:
            result.update({"ok": False, "corr": notes,
                           "fwd_present": fwd is not None, "bwd_present": bwd is not None})
            print(json.dumps(result))
            return
        det_ok, det_notes = _check_determinism(fwd, bwd, scale)
        if not det_ok:
            result.update({"ok": False, "corr": notes, "det": det_notes,
                           "fwd_present": fwd is not None, "bwd_present": bwd is not None})
            print(json.dumps(result))
            return
        m = _measure(fwd, bwd, scale)
        m["ok"] = True
        m["corr"] = notes
        m["det"] = det_notes
        result = m
    except Exception as e:
        result = {"ok": False, "score": 0.0, "tflops": 0.0, "err": f"{type(e).__name__}: {str(e)[:300]}",
                  "tb": traceback.format_exc()[-1500:]}
    print(json.dumps(result))


if __name__ == "__main__":
    main()
