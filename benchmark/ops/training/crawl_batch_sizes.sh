#!/usr/bin/env bash
#
# Crawl per-(GPU, model, dtype) training batch sizes from Primus pretrain
# configs and (re)generate benchmark/ops/training/batch_size_config.json.
#
# The generated JSON records the exact Primus commit it was crawled from so the
# benchmark batch sizes can be tracked back to a specific upstream revision.
#
# Usage:
#   ./crawl_batch_sizes.sh [REF] [OUTPUT_JSON]
#     REF          Primus git ref to crawl (branch / tag / commit). Default: main
#     OUTPUT_JSON  Output path. Default: <script_dir>/batch_size_config.json
#
# Batch-size field per framework:
#   megatron   yamls -> micro_batch_size
#   torchtitan yamls -> local_batch_size
#
# Requirements: bash 4+, curl, awk, python3 (no jq needed).
#
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PRIMUS_REPO="AMD-AGI/Primus"
REF="${1:-main}"
OUT="${2:-${SCRIPT_DIR}/batch_size_config.json}"
RAW_BASE="https://raw.githubusercontent.com/${PRIMUS_REPO}/${REF}"

DTYPES=(bf16 fp8)
declare -A DTYPE_TAG=([bf16]=BF16 [fp8]=FP8)

# GPU config key -> Primus config folder name.
declare -A GPU_FOLDER=([MI35*]=MI355X [MI30*]=MI300X)

# Per-framework yaml field that holds the per-GPU micro batch size.
declare -A FRAMEWORK_FIELD=([megatron]=micro_batch_size [torchtitan]=local_batch_size)

# Standard configs follow "<basename>-<DTYPE>-pretrain.yaml" and are generated in
# a loop over GPU x dtype. Each framework keeps its own model -> basename map so
# cross-framework naming differences are matched verbosely (e.g. Llama-4 is
# "llama4_17B128E" on megatron but "llama4_17Bx128E" on torchtitan).
# shellcheck disable=SC2034 # Accessed indirectly through crawl_convention's nameref.
declare -A MEGATRON_BASENAME=(
  [Llama-3.1-405B]=llama3.1_405B
  [Llama-3.1-8B]=llama3.1_8B
  [Llama-2-7B]=llama2_7B
  [Llama-2-70B]=llama2_70B
  [Qwen2.5-7B]=qwen2.5_7B
  [Qwen2.5-72B]=qwen2.5_72B
  [DeepSeek-V3]=deepseek_v3
  [DeepSeek-V2]=deepseek_v2
  [DeepSeek-V2-Lite]=deepseek_v2_lite
  [Grok-2]=grok2
  [Mixtral-8x7B]=mixtral_8x7B_v0.1
  [Mixtral-8x22B]=mixtral_8x22B_v0.1
  [Qwen3-30B-A3B]=qwen3_30B_A3B
  [Qwen3-235B-A22B]=qwen3_235B_A22B
  [Llama-4-17Bx16E]=llama4_17B16E
  [Llama-4-17Bx128E]=llama4_17B128E
)

# shellcheck disable=SC2034 # Accessed indirectly through crawl_convention's nameref.
declare -A TORCHTITAN_BASENAME=(
  [Llama-3.1-405B]=llama3.1_405B
  [Llama-3.1-8B]=llama3.1_8B
  [Llama-4-17Bx16E]=llama4_17Bx16E
  [Llama-4-17Bx128E]=llama4_17Bx128E
)

# Appendix: outlier configs that omit the "-<DTYPE>" tag and therefore cannot be
# generated by the standard loop. Their precision is set by internal yaml flags
# (e.g. deepseek_v3_671b has use_turbo_float8_linear: true -> FP8 linear GEMMs),
# so the (model, dtype) pairing is pinned explicitly here.
#
# To stay robust if upstream later fixes the naming, the path field is a
# comma-separated list of candidates tried in order; the first one that exists
# wins. The convention-compliant path is listed FIRST so that once upstream adds
# it, we automatically switch to it without editing this script.
#   "framework|gpu|model|dtype|preferred_path[,fallback_path,...]"
APPENDIX_SOURCES=(
  "torchtitan|MI35*|DeepSeek-V3|fp8|examples/torchtitan/configs/MI355X/deepseek_v3_671b-FP8-pretrain.yaml,examples/torchtitan/configs/MI355X/deepseek_v3_671b-pretrain.yaml"
  "torchtitan|MI30*|DeepSeek-V3|fp8|examples/torchtitan/configs/MI300X/deepseek_v3_671b-FP8-pretrain.yaml,examples/torchtitan/configs/MI300X/deepseek_v3_671b-pretrain.yaml"
)

TMP="$(mktemp)"
trap 'rm -f "$TMP"' EXIT

# extract_field <field> <url>
#   Prints the first integer value of "<field>:" found in the fetched yaml.
#   Returns non-zero only if the URL could not be fetched.
extract_field() {
  local field="$1" url="$2" body
  if ! body="$(curl -fsSL "$url" 2>/dev/null)"; then
    return 1
  fi
  awk -v f="${field}:" '$1==f {gsub(/[^0-9]/, "", $2); print $2; exit}' <<<"$body"
}

# record_source <framework> <gpu> <model> <dtype> <relpath> [relpath...]
#   Tries each candidate relpath in order and records the first that yields a
#   value. Listing candidates makes outlier handling robust to upstream renames.
record_source() {
  local framework="$1" gpu="$2" model="$3" dtype="$4"
  shift 4
  local field="${FRAMEWORK_FIELD[$framework]}" rel val
  for rel in "$@"; do
    if val="$(extract_field "$field" "${RAW_BASE}/${rel}")" && [[ -n "$val" ]]; then
      printf '%s\t%s\t%s\t%s\t%s\t%s\n' "$gpu" "$model" "$dtype" "$framework" "$rel" "$val" >>"$TMP"
      echo "  [ok]   $gpu $model $dtype  $framework $field=$val ($rel)" >&2
      return 0
    fi
  done
  echo "  [skip] missing/no field: $*" >&2
}

# crawl_convention <framework> <basename_map_name>
#   Generates "<basename>-<DTYPE>-pretrain.yaml" sources over all GPU x dtype.
crawl_convention() {
  local framework="$1"
  local -n basename_map="$2"
  local gpu folder model base dtype tag rel
  for gpu in "${!GPU_FOLDER[@]}"; do
    folder="${GPU_FOLDER[$gpu]}"
    for model in "${!basename_map[@]}"; do
      base="${basename_map[$model]}"
      for dtype in "${DTYPES[@]}"; do
        tag="${DTYPE_TAG[$dtype]}"
        rel="examples/${framework}/configs/${folder}/${base}-${tag}-pretrain.yaml"
        record_source "$framework" "$gpu" "$model" "$dtype" "$rel"
      done
    done
  done
}

echo "Crawling Primus@${REF} from ${RAW_BASE} ..." >&2

# Standard convention sources (looped per framework).
crawl_convention megatron MEGATRON_BASENAME
crawl_convention torchtitan TORCHTITAN_BASENAME

# Appendix: suffix-less outliers (each declares candidate paths, first hit wins).
for entry in "${APPENDIX_SOURCES[@]}"; do
  IFS='|' read -r framework gpu model dtype candidates <<<"$entry"
  IFS=',' read -r -a candidate_paths <<<"$candidates"
  record_source "$framework" "$gpu" "$model" "$dtype" "${candidate_paths[@]}"
done

# --- resolve the exact Primus commit we crawled ---------------------------
COMMIT="$(curl -fsSL "https://api.github.com/repos/${PRIMUS_REPO}/commits/${REF}" \
  | python3 -c 'import sys, json; print(json.load(sys.stdin)["sha"])')"
CRAWLED_AT="$(date -u +%Y-%m-%dT%H:%M:%SZ)"

# --- assemble JSON --------------------------------------------------------
PRIMUS_REPO="$PRIMUS_REPO" REF="$REF" COMMIT="$COMMIT" CRAWLED_AT="$CRAWLED_AT" \
  python3 - "$TMP" "$OUT" <<'PY'
import json
import os
import sys
from collections import OrderedDict

tmp_path, out_path = sys.argv[1], sys.argv[2]

entries = OrderedDict()  # (gpu, model, dtype) -> entry dict
with open(tmp_path) as fh:
    for line in fh:
        line = line.rstrip("\n")
        if not line:
            continue
        gpu, model, dtype, framework, path, value = line.split("\t")
        value = int(value)
        key = (gpu, model, dtype)
        entry = entries.setdefault(
            key,
            {"gpu": gpu, "model": model, "dtype": dtype, "batch_sizes": set(), "sources": []},
        )
        entry["batch_sizes"].add(value)
        entry["sources"].append({"framework": framework, "path": path, "value": value})

records = []
for key in sorted(entries):
    entry = entries[key]
    entry["batch_sizes"] = sorted(entry["batch_sizes"])
    entry["sources"] = sorted(entry["sources"], key=lambda s: (s["framework"], s["path"]))
    records.append(entry)

doc = OrderedDict()
doc["_meta"] = {
    "description": "Per-(GPU, model, dtype) training batch sizes crawled from Primus pretrain configs.",
    "source_repo": f"https://github.com/{os.environ['PRIMUS_REPO']}",
    "source_ref": os.environ["REF"],
    "primus_commit": os.environ["COMMIT"],
    "crawled_at": os.environ["CRAWLED_AT"],
    "batch_size_fields": {"megatron": "micro_batch_size", "torchtitan": "local_batch_size"},
    "generated_by": "benchmark/ops/training/crawl_batch_sizes.sh",
}
doc["default"] = [1, 2, 4]
doc["dtype_default"] = {"bf16": [1, 2, 4], "fp8": [1, 2, 4]}
doc["entries"] = records

with open(out_path, "w") as fh:
    json.dump(doc, fh, indent=2)
    fh.write("\n")

print(
    f"Wrote {out_path}: {len(records)} entries, Primus {os.environ['COMMIT'][:12]}",
    file=sys.stderr,
)
PY

echo "Done." >&2
