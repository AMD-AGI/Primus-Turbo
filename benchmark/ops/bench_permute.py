def _write_if_changed(path: Path, content: str) -> None:
    """Write ``content`` to ``path`` only when stale, so ninja doesn't trigger
    a needless rebuild on every script invocation."""
    if path.exists():
        try:
            if path.read_text() == content:
                return
        except OSError:
            pass
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)


def _load_permute_ops_jit():
    """JIT-compile ``csrc/kernels/permute/permute.cu`` against an inline
    pybind11 shim into a self-contained cached extension. ninja keeps the
    build incremental so subsequent iterations only recompile what changed.

    The shim source (``permute_jit_bind.cu``) is auto-generated from
    ``_PERMUTE_JIT_BIND_CU`` above and written to the cache directory; it is
    only re-emitted when the content changes, so unchanged invocations skip
    the recompile.

    Cache location and module name are overridable via env vars:

      * ``PRIMUS_TURBO_PERMUTE_JIT_DIR``  — cache root
        (default: ``<repo>/build/jit/permute``)
      * ``PRIMUS_TURBO_PERMUTE_JIT_TAG``  — build subdir tag
        (default: ``build``)
      * ``PRIMUS_TURBO_PERMUTE_JIT_MOD``  — Python module name
        (default: ``primus_turbo_permute_jit``)
    """
    from torch.utils import cpp_extension

    default_jit_dir = _REPO_ROOT / "build/jit/permute"
    jit_root = Path(os.environ.get("PRIMUS_TURBO_PERMUTE_JIT_DIR", str(default_jit_dir)))
    build_tag = os.environ.get("PRIMUS_TURBO_PERMUTE_JIT_TAG", "build")
    module_name = os.environ.get("PRIMUS_TURBO_PERMUTE_JIT_MOD", "primus_turbo_permute_jit")
    bind_dir = jit_root
    build_dir = bind_dir / build_tag
    build_dir.mkdir(parents=True, exist_ok=True)

    bind_path = bind_dir / "permute_jit_bind.cu"
    _write_if_changed(bind_path, _PERMUTE_JIT_BIND_CU)

    sources = [
        str(_REPO_ROOT / "csrc/kernels/permute/permute.cu"),
        str(bind_path),
    ]
    extra_include_paths = [
        str(_REPO_ROOT / "csrc"),
        str(_REPO_ROOT / "csrc/include"),
    ]
    extra_cflags = ["-O3", "-std=c++20"]
    # The ``-U__HIP_NO_*`` undefs are critical: PyTorch's HIP cpp_extension
    # path defines ``__HIP_NO_HALF_CONVERSIONS__`` etc. by default, which
    # makes ``primus_turbo/float8.h``'s implicit ``operator half()`` /
    # ``operator bfloat16()`` conversions fail to compile.
    extra_cuda_cflags = [
        "-O3",
        "-std=c++20",
        "-fno-gpu-rdc",
        "-fno-offload-uniform-block",
        "-DPRIMUS_TURBO_GFX942",
        "-U__HIP_NO_HALF_OPERATORS__",
        "-U__HIP_NO_HALF_CONVERSIONS__",
        "-U__HIP_NO_BFLOAT16_OPERATORS__",
        "-U__HIP_NO_BFLOAT16_CONVERSIONS__",
        "-U__HIP_NO_BFLOAT162_OPERATORS__",
        "-U__HIP_NO_BFLOAT162_CONVERSIONS__",
    ]
    print(f"[bench] JIT-compiling permute extension into {build_dir} (mod={module_name})")
    return cpp_extension.load(
        name=module_name,
        sources=sources,
        extra_include_paths=extra_include_paths,
        extra_cflags=extra_cflags,
        extra_cuda_cflags=extra_cuda_cflags,
        build_directory=str(build_dir),
        verbose=True,
    )


def _load_permute_ops_prebuilt():
    _CSO_GLOB = list((_REPO_ROOT / "primus_turbo" / "pytorch").glob("_C*.so"))
    if not _CSO_GLOB:
        raise RuntimeError(
            f"Could not locate primus_turbo._C extension under {_REPO_ROOT}/primus_turbo/pytorch. "
            f"Run `pip install --no-build-isolation -e .` first, or set "
            f"PRIMUS_TURBO_PERMUTE_JIT=1 to JIT-compile permute.cu directly."
        )
    os.environ.setdefault(
        "LD_LIBRARY_PATH",
        f"{_REPO_ROOT / 'primus_turbo' / 'lib'}:{os.environ.get('LD_LIBRARY_PATH', '')}",
    )
    torch.ops.load_library(str(_CSO_GLOB[0]))
    return torch.ops.primus_turbo_cpp_extension


