"""Benchmark for the MoE permute / unpermute paths in Primus-Turbo.

Compares two backends side-by-side on the same DeepEP-style dispatch workloads:

  * ``cuda``   - HIP single-kernel decoupled-lookback preprocessing kernel
                 plus the vectorised data-movement kernels in
                 ``csrc/kernels/permute/permute.cu``. Driven via
                 ``primus_turbo.pytorch.ops.moe.permute_preprocessing`` /
                 ``permute`` / ``unpermute``.
  * ``triton`` - Triton kernels in ``primus_turbo/triton/moe/permutation.py``
                 (``make_row_id_map`` + ``permute_with_mask_map`` +
                 ``unpermute_with_mask_map``).

Test cases are driven by ``benchmark/ops/config.py::MoEModelConfigs``,
expanded over ``BATCH_SIZE_LIST x GROUPED_GEMM_EP_SIZE_LIST``.

Permute inputs (``recv_topk_idx`` / ``recv_num_tokens``) simulate the output of
DeepEP intranode dispatch (see ``benchmark/ops/deep_ep/test_intranode.py``):

  * Each source rank has ``N`` tokens, each token picks ``K`` experts uniformly
    without replacement out of ``E`` global experts.
  * DeepEP deduplicates: a token enters this rank iff at least one of its
    experts lies in the local range ``[0, E/R)``.

  E[recv_num_tokens] = R * N * (1 - C(E - E/R, K) / C(E, K))

  E.g. ``E=256, R=8, K=8, N=4096`` -> ``~21622`` (~0.66 * R * N).

Usage::

    python benchmark/ops/bench_permute.py
    python benchmark/ops/bench_permute.py -o out.csv
    python benchmark/ops/bench_permute.py --backends cuda
    python benchmark/ops/bench_permute.py --backends triton
"""

