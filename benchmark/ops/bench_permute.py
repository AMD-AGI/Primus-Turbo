# don't have to thread `num_of_blocks_permute` through 4 callers. Set
# `PRIMUS_TURBO_PERMUTE_NUM_BLOCKS=608` (or any positive int) to bypass
# the C++ default `default_grid_size = num_cu (=304 on MI300X)`.
def _resolve_num_of_blocks_permute(default: int = 0) -> int:
    raw = os.environ.get("PRIMUS_TURBO_PERMUTE_NUM_BLOCKS", "").strip()
    if not raw:
        return default
    try:
        v = int(raw)
        return v if v > 0 else default
    except ValueError:
        return default


